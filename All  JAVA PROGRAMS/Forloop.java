
import java.util.Scanner;

 class Forloop {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        
        for (int i = 1; i <= 4; i++)
         {
            for (int j = 1; j <= 4; j++)
            {
                System.out.println(i+" "+j);
            }
            
        }
    
       
    }
}
